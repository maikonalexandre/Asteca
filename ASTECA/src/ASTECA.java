    private void btnE_E1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnE_E1ActionPerformed
        tfE_C.setText("GERADO PELO SISTEMA");
        if (tblE.getSelectedRow() != -1) {
            Especie e = new Especie();
            EspecieDAO dao = new EspecieDAO();
            e.setEspecie((String) tblE.getValueAt(tblE.getSelectedRow(), 0));
            dao.delete(e);
            tfE_E.setText("");
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma espécie para excluir.");
        }
    }//GEN-LAST:event_btnE_E1ActionPerformed
