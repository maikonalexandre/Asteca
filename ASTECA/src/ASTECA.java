    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblE.getModel();
        modelo.setNumRows(0);
        EspecieDAO edao = new EspecieDAO();
        for (Especie e : edao.read()) {
            modelo.addRow(new Object[]{
                e.getEspecie(),
                e.getCodEspecie(),});
        }
    }

    public void readJTableR() {
        DefaultTableModel modelo = (DefaultTableModel) tblR.getModel();
        modelo.setNumRows(0);
        RacaDAO edao = new RacaDAO();
        for (Raca r : edao.readr()) {
            modelo.addRow(new Object[]{
                r.getEspecie(),
                r.getCodRaca(),});
        }
    }
    public void readTblE(String esp) {
        DefaultTableModel modelo = (DefaultTableModel) tblE.getModel();
        modelo.setNumRows(0);
        EspecieDAO edao = new EspecieDAO();
        for (Especie e : edao.readEspecie(esp)) {
            modelo.addRow(new Object[]{
                e.getEspecie(),});
        }
    }
